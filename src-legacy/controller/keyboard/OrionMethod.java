package org.ardenus.input.controller.keyboard;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Used to signify that a method will only work if a Logitech Orion product is
 * being used.
 * <p>
 * While the method might not work if a Logitech Orion product is not being
 * used, it does not mean that calling the method will cause the program to
 * crash.
 *
 * @author Trent Summerlin
 * @since Ardensu Engine v0.0.1-SNAPSHOT
 */
@Documented
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.SOURCE)
public @interface OrionMethod {
}
